// operating with variables
#include <iostream>
using namespace std;
int main () 
{ 
// declaring variables:
int a=100, b=20;  
// process:
cout <<"Numbers are "<<a<<" "<<b<<endl;
cout <<"Adding the numbers, we get "<<a<<" + "<<b<<" = "<<a+b<<endl;
cout <<"Subtracting the numbers, we get "<<a<<" - "<<b<<" = "<<a-b<<endl;
cout <<"Multiplying the numbers, we get "<<a<<" * "<<b<<" = "<<a*b<<endl;
cout <<"Dividing the numbers, we get "<<a<<" / "<<b<<" = "<<a/b<<endl;
// terminate the program:
return 0; 
} 
